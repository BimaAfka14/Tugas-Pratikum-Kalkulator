    private void hasilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hasilActionPerformed
        // TODO add your handling code here:
        double hasilHitung = 0.0;
        bilangan2 = Double.parseDouble(angka);
    
    switch (pilih) {
        case 1:
            hasilHitung = bilangan1 + bilangan2;
            break;
        case 2:
            hasilHitung = bilangan1 - bilangan2;
            break;
        case 3:
            hasilHitung = bilangan1 * bilangan2;
            break;
        case 4:
            hasilHitung = bilangan1 / bilangan2;
            break;
        case 5:
            hasilHitung = bilangan1 % bilangan2;
            break;
        default:
            break;
    }
    
    // Tampilkan hasil perhitungan di tampilan
    tampilan.setText(String.valueOf(hasilHitung));
    
    // Perbarui bilangan1 dengan hasil perhitungan sebelumnya
    bilangan1 = hasilHitung;
    
    // Reset variabel angka dan pilih untuk inputan berikutnya
    angka = "";
    pilih = 0;
    }//GEN-LAST:event_hasilActionPerformed

