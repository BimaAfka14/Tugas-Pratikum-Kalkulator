    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        // Ambil teks dari tampilan
    // Ambil teks dari tampilan
    String currentText = tampilan.getText();
    
    // Periksa apakah teks tidak kosong
    if (!currentText.isEmpty()) {
        // Hapus karakter terakhir dari teks
        String newText = currentText.substring(0, currentText.length() - 1);
        
        // Periksa apakah setelah menghapus karakter terakhir, teks menjadi kosong
        if (newText.isEmpty()) {
            tampilan.setText("0"); // Jika teks kosong, setel tampilan menjadi 0
            angka = "";
        } else {
            tampilan.setText(newText); // Jika tidak kosong, setel tampilan ke teks yang telah diubah
            angka = newText;
        }
    }
    }//GEN-LAST:event_deleteActionPerformed

